#pragma once

/**
 * @brief Type definition for a constant unsigned long duration.
 */
typedef const unsigned long dur_t;

/**
 * @brief External declarations of time duration constants.
 * 
 * These constants represent the duration of a millisecond, second, minute, and hour.
 */
extern dur_t MILLISECOND;
extern dur_t SECOND;
extern dur_t MINUTE;
extern dur_t HOUR;
